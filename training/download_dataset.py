"""
Download and prepare Naruto hand seal datasets from Roboflow and Kaggle.
Merges multiple sources, deduplicates, and organizes into YOLO format.
"""

import os
import shutil
import argparse
from pathlib import Path

try:
    from roboflow import Roboflow
except ImportError:
    print("Please install roboflow: pip install roboflow")
    exit(1)


CLASSES = [
    "bird", "boar", "dog", "dragon", "hare", "horse",
    "monkey", "ox", "ram", "rat", "snake", "tiger"
]


def download_roboflow_dataset(api_key: str, workspace: str, project: str, 
                                version: int, output_dir: str, format: str = "yolov8"):
    """Download a dataset from Roboflow."""
    rf = Roboflow(api_key=api_key)
    proj = rf.workspace(workspace).project(project)
    dataset = proj.version(version).download(format, location=output_dir)
    print(f"Downloaded dataset to: {output_dir}")
    return dataset


def merge_datasets(source_dirs: list[str], output_dir: str):
    """
    Merge multiple YOLO-format datasets into a single dataset.
    Handles deduplication by checking file names.
    """
    output_path = Path(output_dir)
    
    for split in ["train", "valid", "test"]:
        img_dir = output_path / split / "images"
        lbl_dir = output_path / split / "labels"
        img_dir.mkdir(parents=True, exist_ok=True)
        lbl_dir.mkdir(parents=True, exist_ok=True)
        
        seen_files = set()
        file_count = 0
        
        for source_dir in source_dirs:
            source_path = Path(source_dir)
            
            # Try different directory structures
            for img_subdir in [f"{split}/images", "images", split]:
                src_img_dir = source_path / img_subdir
                if src_img_dir.exists():
                    break
            else:
                print(f"  Skipping {source_dir}/{split} — no images directory found")
                continue
            
            # Find corresponding labels directory
            lbl_subdir = img_subdir.replace("images", "labels")
            src_lbl_dir = source_path / lbl_subdir
            
            for img_file in src_img_dir.iterdir():
                if img_file.suffix.lower() not in [".jpg", ".jpeg", ".png", ".bmp"]:
                    continue
                
                # Deduplicate by content hash (simple: by filename for now)
                if img_file.name in seen_files:
                    continue
                seen_files.add(img_file.name)
                
                # Copy image
                shutil.copy2(img_file, img_dir / img_file.name)
                
                # Copy label if exists
                lbl_file = src_lbl_dir / f"{img_file.stem}.txt"
                if lbl_file.exists():
                    shutil.copy2(lbl_file, lbl_dir / lbl_file.name)
                
                file_count += 1
        
        print(f"  {split}: {file_count} images")
    
    print(f"Merged dataset saved to: {output_dir}")


def create_data_yaml(output_dir: str):
    """Create data.yaml for YOLO training."""
    output_path = Path(output_dir)
    data_yaml = output_path / "data.yaml"
    
    content = f"""# Naruto Hand Seal Detection Dataset
# Auto-generated by download_dataset.py

path: {output_path.resolve()}
train: train/images
val: valid/images
test: test/images

nc: {len(CLASSES)}
names: {CLASSES}
"""
    data_yaml.write_text(content)
    print(f"Created data.yaml at: {data_yaml}")
    return str(data_yaml)


def main():
    parser = argparse.ArgumentParser(description="Download Naruto hand seal datasets")
    parser.add_argument("--api-key", type=str, required=True,
                        help="Roboflow API key (get from https://app.roboflow.com/settings/api)")
    parser.add_argument("--output", type=str, default="./datasets/naruto-hand-seals",
                        help="Output directory for merged dataset")
    parser.add_argument("--skip-download", action="store_true",
                        help="Skip download, only merge existing datasets")
    args = parser.parse_args()

    output_dir = Path(args.output)
    raw_dir = output_dir / "raw"
    raw_dir.mkdir(parents=True, exist_ok=True)

    source_dirs = []

    if not args.skip_download:
        print("=" * 60)
        print("Downloading Naruto hand seal datasets from Roboflow...")
        print("=" * 60)
        
        # Dataset 1: "Hands Seals Naruto" — well-labeled, 12 classes
        print("\n[1/2] Downloading 'Hands Seals Naruto' by yylunxie...")
        try:
            ds1_dir = str(raw_dir / "roboflow-yylunxie")
            download_roboflow_dataset(
                api_key=args.api_key,
                workspace="yylunxie",
                project="hands-seals-naruto",
                version=1,
                output_dir=ds1_dir
            )
            source_dirs.append(ds1_dir)
        except Exception as e:
            print(f"  Warning: Failed to download dataset 1: {e}")

        # Dataset 2: "Naruto hand sign" by vgu — additional variety
        print("\n[2/2] Downloading 'Naruto hand sign' dataset...")
        try:
            ds2_dir = str(raw_dir / "roboflow-vgu")
            download_roboflow_dataset(
                api_key=args.api_key,
                workspace="vgu-mbah7",
                project="naruto-hand-sign-oqrse",
                version=1,
                output_dir=ds2_dir
            )
            source_dirs.append(ds2_dir)
        except Exception as e:
            print(f"  Warning: Failed to download dataset 2: {e}")
    else:
        # Use existing raw directories
        for d in raw_dir.iterdir():
            if d.is_dir():
                source_dirs.append(str(d))
    
    if not source_dirs:
        print("ERROR: No datasets downloaded. Check your API key and try again.")
        return

    # Merge datasets
    print("\n" + "=" * 60)
    print("Merging datasets...")
    print("=" * 60)
    merged_dir = str(output_dir / "merged")
    merge_datasets(source_dirs, merged_dir)

    # Create data.yaml
    print("\n" + "=" * 60)
    print("Creating data.yaml...")
    print("=" * 60)
    data_yaml_path = create_data_yaml(merged_dir)

    print("\n" + "=" * 60)
    print("DONE!")
    print(f"Dataset ready at: {merged_dir}")
    print(f"Data config at:   {data_yaml_path}")
    print(f"\nNext step: python train.py --data {data_yaml_path}")
    print("=" * 60)


if __name__ == "__main__":
    main()
